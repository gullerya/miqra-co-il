customElements.define('svg-icon', class extends HTMLElement {
	constructor() {
		super();
		this.attachShadow({ mode: 'open' });
	}

	connectedCallback() {
		this.directionObserver();
		new MutationObserver(() => this.directionObserver()).observe(document.body, { attributes: true, attributeFilter: ['dir'], characterData: false });
		if (this.hasAttribute('type')) {
			this.type = this.getAttribute('type');
		}
	}

	directionObserver() {
		if (document.body.getAttribute('dir') === 'rtl') {
			this.classList.add('rtl');
		} else {
			this.classList.remove('rtl');
		}
	}

	set type(type) {
		if (this[typeKey] === type) {
			return;
		}
		if (typeof type !== 'string' && type !== null) {
			throw new Error('icon type MUST be a NON NULL nor EMPTY string');
		}
		this[typeKey] = type;

		//	removing the existing icon anyway
		while (this.lastElementChild) {
			this.shadowRoot.removeChild(this.lastElementChild);
		}

		//	adding new one or printing an error
		if (templatesMap[type]) {
			const
				base = templateBase.content.cloneNode(true),
				icon = templatesMap[type].content.cloneNode(true);
			this.shadowRoot.appendChild(base);
			this.shadowRoot.appendChild(icon);
		} else if (type !== null) {
			console.error('icon type "' + type + '" is undefined');
		}
	}
});
